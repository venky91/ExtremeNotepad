package com.example.extremenotepad;

public class Note {
	
	String title;
	String note;
	
	public Note( String title, String note ) {
		
		this.title = title;
		this.note = note;
		
	}
	
	public void setTitle( String title ) {
		
		this.title = title;
		
	}
	
	public void setNote( String note ) {
		
		this.note = note;
		
	}

}
